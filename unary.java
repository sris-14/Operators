public class unary {
    public static void main(String args[]){
        /*
        Unary operator :
        1. predecrement i.e. --a
        2. postdecrement i.e. a--
        3. preincrement i.e. ++a
        4. postincrement i.e. a++
        */ 
        int a=10;
        int b=--a;
        System.out.println(a);
        System.out.println(b);
    }
}